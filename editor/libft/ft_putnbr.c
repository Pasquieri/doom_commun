#include "libft.h"

void	ft_putnbr(int n)
{
	int div;

	div = 1;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	while (n / div >= 10)
		div = div * 10;
	while (div > 0)
	{
		ft_putchar((n / div) % 10 + '0');
		div = div / 10;
	}
}
